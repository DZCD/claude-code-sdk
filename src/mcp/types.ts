/**
 * ClaudeCode SDK — MCP Protocol Types
 *
 * Type definitions for MCP (Model Context Protocol) server configuration
 * and integration with the SDK. Uses @modelcontextprotocol/sdk internally,
 * but exposes simplified configuration types for SDK users.
 */

// ========== Connection & Tool Types (Phase 1) ==========

/**
 * Configuration for connecting to a single MCP server.
 * Supports stdio (local process) and URL-based (remote) servers.
 */
export interface MCPServerConfig {
  /** Unique name for this server connection */
  name: string

  /**
   * Server type.
   * - 'stdio': Launch a local process (e.g., npx, python)
   * - 'url': Connect to a remote HTTP/SSE endpoint
   */
  type: 'stdio' | 'url'

  /**
   * For 'stdio' type: the command to execute (e.g., 'npx', 'python')
   * For 'url' type: the server endpoint URL (e.g., 'https://mcp.example.com')
   */
  commandOrUrl: string

  /** Arguments for stdio command (ignored for URL type) */
  args?: string[]

  /** Environment variables for stdio process (ignored for URL type) */
  env?: Record<string, string>

  /** Authorization token for remote servers (used with 'url' type) */
  authorizationToken?: string

  /** Optional tool configuration — controls which tools are accessible */
  toolConfiguration?: MCPServerToolConfiguration
}

/** Controls which tools from a server are accessible */
export interface MCPServerToolConfiguration {
  /** Whether to enable tools from this server (default: true) */
  enabled?: boolean
  /** List of specific tool names to allow (empty = allow all) */
  allowedTools?: string[]
}

/** Internal representation of a connected MCP server */
export interface MCPConnection {
  serverName: string
  tools: MCPToolDefinition[]
  capabilities: string[]
}

/** MCP tool definition (simplified from MCP protocol) */
export interface MCPToolDefinition {
  name: string
  description?: string
  inputSchema: Record<string, unknown>
}

/** Error throw by MCP operations */
export class MCPServerError extends Error {
  constructor(
    message: string,
    public readonly serverName: string,
    public override readonly cause?: unknown,
  ) {
    super(`MCP Server "${serverName}": ${message}`)
    this.name = 'MCPServerError'
  }
}

// ========== Resource Types (Phase 2) ==========

/**
 * Definition of a resource available from an MCP server.
 */
export interface MCPResourceDefinition {
  /** Resource URI (unique identifier) */
  uri: string
  /** Human-readable resource name */
  name: string
  /** Optional description of the resource */
  description?: string
  /** MIME type of the resource (e.g., 'text/markdown', 'image/png') */
  mimeType?: string
  /** Name of the server that provides this resource */
  server: string
}

/**
 * Content of a specific resource read from an MCP server.
 */
export interface MCPResourceContent {
  /** Resource URI */
  uri: string
  /** MIME type of the content */
  mimeType?: string
  /** Text content (present for text-based resources) */
  text?: string
  /** Base64-encoded blob content (present for binary resources) */
  blob?: string
}

// ========== Prompt Types (Phase 2) ==========

/**
 * Definition of a prompt template available from an MCP server.
 */
export interface MCPPromptDefinition {
  /** Prompt name (identifier) */
  name: string
  /** Optional description of the prompt */
  description?: string
  /** Optional list of arguments the prompt accepts */
  arguments?: MCPPromptArgument[]
  /** Name of the server that provides this prompt */
  server: string
}

/**
 * Argument definition for an MCP prompt template.
 */
export interface MCPPromptArgument {
  /** Argument name */
  name: string
  /** Optional description of the argument */
  description?: string
  /** Whether the argument is required (default: false) */
  required?: boolean
}

/**
 * Result of getting a prompt template with arguments applied.
 */
export interface MCPGetPromptResult {
  /** Optional description of the rendered prompt */
  description?: string
  /** The messages generated by the prompt template */
  messages: Array<{
    role: string
    content: unknown
  }>
}
